"""## 🛠️ 4. Preprocessing"""

# ── Work on a copy ──
data = df.copy()

# ── 4.1 Parse Date Features ──
data['date'] = pd.to_datetime(data['date'])
data['month']      = data['date'].dt.month
data['day_of_week'] = data['date'].dt.dayofweek   # 0=Mon, 6=Sun
data['day_of_year'] = data['date'].dt.dayofyear
data['is_weekend']  = (data['day_of_week'] >= 5).astype(int)
data['quarter']     = data['date'].dt.quarter

# ── 4.2 Parse Departure Time Features ──
data['hour'] = pd.to_datetime(data['departure_time'], format='%H:%M').dt.hour
data['is_morning']   = ((data['hour'] >= 5)  & (data['hour'] < 12)).astype(int)
data['is_afternoon'] = ((data['hour'] >= 12) & (data['hour'] < 17)).astype(int)
data['is_evening']   = ((data['hour'] >= 17) & (data['hour'] < 22)).astype(int)
data['is_night']     = ((data['hour'] >= 22) | (data['hour'] <  5)).astype(int)
# Cyclical encoding for hour (captures 23→0 circularity)
data['hour_sin'] = np.sin(2 * np.pi * data['hour'] / 24)
data['hour_cos'] = np.cos(2 * np.pi * data['hour'] / 24)

# ── 4.3 Encode Categorical Features ──
le_route    = LabelEncoder()
le_aircraft = LabelEncoder()
le_cabin    = LabelEncoder()

data['route_enc']    = le_route.fit_transform(data['route'])
data['aircraft_enc'] = le_aircraft.fit_transform(data['aircraft_type'])
data['cabin_enc']    = le_cabin.fit_transform(data['cabin_mix'])

# ── 4.4 Feature Engineering: Business Rules ──
data['meals_per_pax_ratio']  = data['meals_consumed'] / data['passengers']   # historical consumption
data['waste_meals']          = data['meals_loaded'] - data['meals_consumed']
data['special_meal_ratio']   = data['special_meals'] / data['passengers']

print('✅ Feature engineering complete!')
print(f'Total features created: {data.shape[1]} columns')

# ── 4.5 Define Features (X) and Target (y) ──
# NOTE: We exclude 'meals_consumed' and 'waste_rate_percent' from prediction features
# because in a real deployment, you won't have those BEFORE loading the meals.
# We keep 'meals_per_pax_ratio' as a historical average feature from past flights.

FEATURES = [
    # Core operational
    'passengers', 'special_meals',
    # Encoded categoricals
    'route_enc', 'aircraft_enc', 'cabin_enc',
    # Time features
    'hour', 'hour_sin', 'hour_cos',
    'is_morning', 'is_afternoon', 'is_evening', 'is_night',
    'month', 'day_of_week', 'is_weekend', 'quarter',
    # Engineered
    'special_meal_ratio',
]
TARGET = 'meals_loaded'

X = data[FEATURES].values
y = data[TARGET].values

print(f'Feature matrix X shape: {X.shape}')
print(f'Target y shape: {y.shape}')
print(f'Features used: {FEATURES}')

# ── 4.6 Train / Validation / Test Split (70 / 15 / 15) ──
X_temp, X_test, y_temp, y_test = train_test_split(
    X, y, test_size=0.15, random_state=42
)
X_train, X_val, y_train, y_val = train_test_split(
    X_temp, y_temp, test_size=0.176, random_state=42   # 0.176 of 85% ≈ 15% overall
)

print(f'Train size : {X_train.shape[0]} ({X_train.shape[0]/len(X)*100:.1f}%)')
print(f'Val   size : {X_val.shape[0]}   ({X_val.shape[0]/len(X)*100:.1f}%)')
print(f'Test  size : {X_test.shape[0]}   ({X_test.shape[0]/len(X)*100:.1f}%)')

