# ── Predict meals for new, unseen flights ──
def predict_meals(flights_dict, model=rf, use_scaling=False):
    """
    flights_dict: dict with keys matching FEATURES columns
    """
    df_new = pd.DataFrame(flights_dict)

    # Apply same encoders
    df_new['route_enc']    = le_route.transform(df_new['route'])
    df_new['aircraft_enc'] = le_aircraft.transform(df_new['aircraft_type'])
    df_new['cabin_enc']    = le_cabin.transform(df_new['cabin_mix'])

    # Time parsing
    df_new['hour']         = pd.to_datetime(df_new['departure_time'], format='%H:%M').dt.hour
    df_new['hour_sin']     = np.sin(2 * np.pi * df_new['hour'] / 24)
    df_new['hour_cos']     = np.cos(2 * np.pi * df_new['hour'] / 24)
    df_new['is_morning']   = ((df_new['hour'] >= 5)  & (df_new['hour'] < 12)).astype(int)
    df_new['is_afternoon'] = ((df_new['hour'] >= 12) & (df_new['hour'] < 17)).astype(int)
    df_new['is_evening']   = ((df_new['hour'] >= 17) & (df_new['hour'] < 22)).astype(int)
    df_new['is_night']     = ((df_new['hour'] >= 22) | (df_new['hour'] <  5)).astype(int)

    df_new['date']         = pd.to_datetime(df_new['date'])
    df_new['month']        = df_new['date'].dt.month
    df_new['day_of_week']  = df_new['date'].dt.dayofweek
    df_new['is_weekend']   = (df_new['day_of_week'] >= 5).astype(int)
    df_new['quarter']      = df_new['date'].dt.quarter
    df_new['special_meal_ratio'] = df_new['special_meals'] / df_new['passengers']

    X_new = df_new[FEATURES].values
    if use_scaling:
        X_new = scaler.transform(X_new)

    predictions = model.predict(X_new)
    return np.round(predictions).astype(int)


